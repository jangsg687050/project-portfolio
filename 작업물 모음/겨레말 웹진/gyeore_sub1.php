			<p class="basic_type">남북이 공동으로 만들어온 《겨레말큰사전》은 1989년 문익환 목사의 방북 당시 문 목사가 김일성 주석과의 회담에서 남북 통일 국어사전 편찬의 필요성을 제기한 것이 시초가 되었다. 《겨레말큰사전》은 2005년 2월에 남북 관계자가 금강산에 모여 ‘《겨레말큰사전》 공동편찬위원회 결성식’을 갖고 남북한이 공동보도문을 채택하면서 본격적으로 시작되었다. 《겨레말큰사전》의 편찬 과정은 남측과 북측이 만나 사전의 내용 및 편찬 지침에 합의를 해야 하기 때문에 다른 사전에 비하여 서너 배 정도의 시간과 에너지가 필요한 지난한 작업이다. 그래서 사업 초기에는 사전 완성까지 약 7년의 기간이 걸릴 것으로 예상하였으나 실제로는 예상보다 2배 이상의 시간이 소요되었다. 사업 시작 후 2015년까지 총 25차의 공동회의를 통하여 남북한이 공동으로 사전 편찬 작업을 진행하여 오다가 2021년 현재까지 6년 가까이 공동회의가 열리지 않는 상태로 편찬사업이 지속되고 있다. 다음에서는 현재까지 집필된 사전 원고를 중심으로 《겨레말큰사전》의 특징을 살펴보기로 하자.</p>

			<p class="title_type6">《겨레말큰사전》, 언어 통일에서 언어 통합으로</p>

			<p class="basic_type">2000년대 이전까지 남북 언어에 대한 관점은 남과 북의 언어가 이질화되어 있다는 인식 아래 이질화된 부분을 통일하고 합치는 데에 초점이 맞추어져 있었다. ‘남북한 언어의 통일’이란 남과 북의 언어 이질화를 전제로 한 것이다. 그러나 남과 북의 언어 차이는 분단 이전에도 존재하고 있었던 것이고 방언과 같은 언어 변이형을 통일하려는 노력은 1933년 제정된 〈한글맞춤법통일안〉에서도 찾아볼 수 있다. 남과 북의 언어 이질화는 개별 어휘의 변이형을 하나로 통일하는 과정으로 극복될 수 있는 것은 아니다. 《겨레말큰사전》의 편찬 지침을 보면 남북한 어휘의 차이를, ‘통일’이 아니라 ‘통합’의 차원으로 바라보고 있음을 알 수 있다. 최근 남한의 표준어 정책도 과도한 통일의 시대를 지나 복수 표준어를 인정하는 등 변이형을 수용하는 통합의 방향으로 바뀌고 있다. 《겨레말큰사전》 편찬사업 초기에 사용되었던 ‘남북 통일 국어사전’이라는 표현에서도 알 수 있듯이 《겨레말큰사전》은 남북의 언어 차이에 대한 인식에서 시작되었으나 《겨레말큰사전》의 현재 모습은 남한과 북한의 언어를 하나로 통합하는 사전이다.<br>언어 통합 사전으로서 《겨레말큰사전》의 가장 특징적인 부분은 용례이다. 《겨레말큰사전》에서는 남과 북, 그리고 해외의 문헌 자료로 구축된 말뭉치를 활용하여 해당 올림말의 뜻풀이를 잘 드러내 주는 용례를 제시하였는데 남한과 북한, 중국의 용례를 가능한 범위 안에서 고르게 수록하였다. 해당 용례마다 출전 뒤에 <em class="hcrbatang">‘󰂸’, ‘󰃍’, ‘󰃪’</em> 등의 약물을 주어 해당 올림말이 어느 지역에서 어떠한 용법으로 쓰였는지를 알 수 있게 하였다. 《겨레말큰사전》에서는 지역별 용례를 제시함으로써 우리말의 어휘부가 하나로 통합될 수 있는 바탕을 마련하였다.</p>

