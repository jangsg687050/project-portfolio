			<p class="basic_type">‘동무’는 남한의 《표준국어대사전》과 북한의 《조선말대사전》에 모두 올라 있는 올림말이다. 《표준국어대사전》에는 ‘늘 친하게 어울리는 사람’이, 《조선말대사전》에는 ‘〈혁명대오에서 함께 싸우는 사람〉을 친근하게 이르는 말’이 첫 번째 의미로 기술되어 있는데 이를 통합한 결과가 바로 위의 예이다. 위의 예에서 ③은 《표준국어대사전》에 「북한어」라는 정보가 붙어 있는 뜻풀이로, 《겨레말큰사전》에서는 북한어 정보를 떼고 하나의 올림말 안에 포함하였다. 이러한 통합 과정에 가장 중요한 역할을 한 것은 용례이다. 용례에 어떤 지역에서 쓰이는지가 약물로 표시됨으로써 자연스럽게 해당 뜻풀이의 지역적 분포에 대한 정보를 알 수 있게 되는 것이다.<br>《겨레말큰사전》을 편찬하면서 남과 북은 사전에서 차이를 보였던 자모 순서나 자모 명칭을 하나로 합의한 바 있고, 표준형과 비표준형을 구분하지 않음으로써 모든 올림말에 동등한 가치를 부여하였다. 또한 《겨레말큰사전》은 올림말의 어원, 국어사적 지식 등을 제시한 속구조, [붙임] 정보를 통하여 우리말 어휘가 하나의 뿌리를 가지고 있음을 드러냄으로써 남한과 북한, 그리고 그 외 지역의 어휘를 통합하고 있다.<br>앞으로는 언어 규범이나 정책 위주의 획일적인 언어 통일에서 벗어나 의사소통을 전제로 한 언어 통합의 문제에 관심이 집중될 가능성이 있다. 이러한 통합은 《겨레말큰사전》의 편찬 태도와 궤를 같이하는 것으로 한국어의 자산은 통일 이후 더욱 풍부해질 수 있다.</p>

			<p class="title_type6">《겨레말큰사전》, 완성형 사전인가? 과정형 사전인가?</p>

			<p class="basic_type">국어사전은 언어생활의 전범이며 참조점이다. 국어사전이라고 하면 맞춤법을 확인하고 모르는 낱말의 뜻을 찾는 것을 먼저 떠올린다. 그래서 국어사전 사용자에게 표기법은 매우 중요한 의미를 가진다. 그러나 《겨레말큰사전》은 다른 국어사전과 달리 표기법이 단일하지 않은 사전이다. 《겨레말큰사전》에서는 미합의되어 하나의 표제항에 함께 제시한 올림말은 뜻풀이에서도 어느 한쪽의 표기를 사용하지 않고 가령 ‘노인#로인, 푸주간#푸줏간, 화페#화폐’ 등과 같이 두 형태를 함께 제시하게 된다. 사전 사용자가 볼 때 이러한 형태 표기 양상은 혼란스럽지 않을 수 없다. 그러나 《겨레말큰사전》이 남과 북, 공동의 사전 편찬 작업의 과정을 기록한 사전이고 어느 한쪽의 힘에 의하여 억지로 단일화를 이루려고 한 사전이 아니라는 점을 이해한다면 이러한 표기법은 《겨레말큰사전》의 또 하나의 특징으로 평가될 수 있을 것이다.<br>
			형태 표기의 문제뿐 아니라 올림말의 구성에서도 《겨레말큰사전》은 과정형 사전으로서의 면모를 보이는데 남과 북이 사전에 등재하기로 합의한 33만 개의 올림말 중 30만 7천 개의 올림말이 현재 집필되어 있고 2만 3천 개의 어휘가 들어갈 자리가 남아 있는 상태이다. 조만간 북측이 사전 편찬 작업에 동참하여 2만 3천 개의 새어휘가 집필되는 날이 온다면 《겨레말큰사전》은 완성형에 가까운 모습을 갖출 수 있을 것이다.</p>

			<p class="title_type6">통일 후 우리 민족의 미래: 《겨레말큰사전》</p>

			<p class="basic_type">《겨레말큰사전》은 우리말 사전의 편찬사에 한 획을 긋는 역사적 사건이다. 이 사전은 사전편찬학 분야에서뿐 아니라 언어문화사적인 측면에서도 중요한 위치를 차지할 것으로 보인다. 지난 100년 간 우리말은 때로는 민족자존의 증거로서 지켜야 할 문화 자산이기도 하였고 우리 민족의 생활을 풍요롭게 하는 주요한 의사소통의 도구이기도 하였으며 이제는 남과 북의 분단 언어를 이어주는 다리로서의 역할을 하고 있다. <br>20세기에 우리말의 많은 변이형 중에서 표준형을 정하고 단일화시키는 표준화 작업이 언어 정책의 대종을 이루었다면, 21세기에 들어서는 변종을 보존하고 언어의 다양성을 존중하는 방향으로 정책이 변화되고 있다. 《겨레말큰사전》은 이러한 변화 속에서 통일에서 통합으로 가는 우리말의 모습을 고스란히 담고 있어 더욱 그 의미가 크다. 《겨레말큰사전》이 《조선말대사전》과 《표준국어대사전》이라는 두 개의 표준형에서 출발하였다는 사실은 이 사전이 가지는 현실적인 문제를 내포하면서 이 두 변이형이 공존할 수 있다는 가능성을 보여주는 부분이기도 하다.<br>《겨레말큰사전》은 남한의 언어와 북한의 언어, 그리고 그 외에 우리말이 사용되고 있는 지역의 언어가 함께 통합되는 사전인 동시에 아직 그 통합이 완성되지 않은 채 현재 진행형으로 나아가는 과정형 사전으로서의 의미를 지니고 있다. 《겨레말큰사전》은 현재 우리가 서 있는 지점을 그대로 보여주고 있으며, 또한 우리의 미래를 담고 있는 사전이다. <img class="end_icon" src="/webzine/2020_12/images/icon_end.gif" alt="겨레말"><br/></p>
						
			
			
